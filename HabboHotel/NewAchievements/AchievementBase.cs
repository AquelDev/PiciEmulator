using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Pici.HabboHotel.NewAchievements
{
    struct AchievementBase
    {
        internal uint acievementID;
        internal uint levels;
        internal uint badgelevel;
        internal string badge;
        internal int pixelBase;
        internal int pixelMMPORG;
        internal int pixelReward;
        internal string category;

        public AchievementBase(uint acievementID, uint levels, uint badgelevel, string badge, int pixelBase, int pixelMMPORG, int pixelReward, string category)
        {
            this.acievementID = acievementID;
            this.levels = levels;
            this.badgelevel = badgelevel;
            this.badge = badge;
            this.pixelBase = pixelBase;
            this.pixelMMPORG = pixelMMPORG;
            this.pixelReward = pixelReward;
            this.category = category;
        }
    }
}
