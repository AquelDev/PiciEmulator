using System;

namespace Pici.HabboHotel.RoomBots
{
    class RandomSpeech
    {
        internal string Message;
        internal bool Shout;
        internal UInt32 BotID;

        internal RandomSpeech(string Message, bool Shout, UInt32 BotID)
        {
            this.BotID = BotID;
            this.Message = Message;
            this.Shout = Shout;
        }
    }
}
